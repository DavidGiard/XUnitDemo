namespace DemoXUnit
{
    public class MathFunctions
    {
        public int Add(int x, int y)
        {
            return x + y;
        }

        public int Subtract(int x, int y)
        {
            return x - y;
        }

        public int Multiply(int x, int y)
        {
            return x * y;
        }


        public float Divide (int x, int y)
        {
            return x / y;
        }
    }
}
